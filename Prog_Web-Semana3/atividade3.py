# Problema 3
# Entrada: Ler 3 nomes
nome1 = str(input('Digite o primeiro nome: \n'))
nome2 = str(input('Digite o segundo nome: \n'))
nome3 = str(input('Digite o terceiro nome: \n'))
# Ordendando
lista = [nome1, nome2, nome3]
lista.sort()
# Saida: Uma lista com os nomes em ordem alfabética
print(f'A lista de nomes ordenados é: {lista}')
