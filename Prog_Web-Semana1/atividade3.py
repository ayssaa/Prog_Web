# Elaborar um programa que leia uma força e velocidade e ache a potência
print('Bem vindo(a) ao programa de cálculo de Potência!')
print('Insira os valores para a força e velocidade do corpo:')
# Entrada da força e velocidade
forca = float(input('Forca: '))
velocidade = float(input('Velocidade: '))
# Calculando...
pot = forca * velocidade
# Saida
print(f'A potência tem um valor de: {pot}')
